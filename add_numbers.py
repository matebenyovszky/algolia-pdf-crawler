def add_numbers(a, b):
    """
    Add two numbers and return the result.

    :param a: First number to add.
    :param b: Second number to add.
    :return: Sum of a and b.
    """
    return a + b

if __name__ == "__main__":
    # Example usage:
    num1 = 5
    num2 = 3
    print(f"The sum of {num1} and {num2} is {add_numbers(num1, num2)}.")
